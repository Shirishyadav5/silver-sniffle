package Variables;

public class Sample3
{

     static void main()
    {
//        VARIABLES ARE NOTHING BUT AN PIECE OF MEMORY USE TO STORE INFORMATION.
//        One variable can store 1 information at a time.
//        variables also used in information reusability.
//==========================================================================================
//        To Utilize variable in java programing language we need to follow below steps
//        1.Variable Declaration (Allocating or Reserving Memory)
//        2.Variable Initialization (Assigning or Inserting Value)
//        3.Variable Usage
//        =====================================================================================
//        String - Multiple character/to store name like Student name -"Shirish";
//        Integer -    to store Numeric/integer value   - 101
//        char - use to store only single character value-'A';
//        Double -Use to store Decimal Value - 10.21f;

        String studentname="shirish";

        System.out.println(studentname);

        Integer num=101;
        System.out.println(num);

        char Grade='A';
        System.out.println(Grade);

        Double percentage=10.43;
        System.out.println(percentage);

        float num1= 10.22F;
        System.out.println(num1);

        System.out.println("main method running");
    }
}
