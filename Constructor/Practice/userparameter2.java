package Constructor.Practice;

public class userparameter2
{
    int num1;
    int num2;

    userparameter2(int a, int b)
    {
        num1=a;
        num2=b;
    }

    public void add()
    {
        System.out.println(num1+num2);
    }
    public void multi()
    {
        System.out.println(num1*num2);
    }
}
